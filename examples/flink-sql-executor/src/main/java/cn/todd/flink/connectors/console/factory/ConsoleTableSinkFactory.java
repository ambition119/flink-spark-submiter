package cn.todd.flink.connectors.console.factory;

import cn.todd.flink.connectors.console.sink.ConsoleSink;
import org.apache.flink.configuration.ConfigOption;
import org.apache.flink.configuration.ReadableConfig;
import org.apache.flink.table.api.TableSchema;
import org.apache.flink.table.connector.sink.DynamicTableSink;
import org.apache.flink.table.factories.DynamicTableSinkFactory;
import org.apache.flink.table.factories.FactoryUtil;

import java.util.HashSet;
import java.util.Set;

/**
 * reference PrintTableSinkFactory
 */
public class ConsoleTableSinkFactory implements DynamicTableSinkFactory {

    public static final String IDENTIFIER = "console";

    @Override
    public DynamicTableSink createDynamicTableSink(Context context) {
        FactoryUtil.TableFactoryHelper helper = FactoryUtil.createTableFactoryHelper(this, context);
        helper.validate();
        ReadableConfig options = helper.getOptions();
        TableSchema schema = context.getCatalogTable().getSchema();

        return new ConsoleSink(options.getOptional(FactoryUtil.SINK_PARALLELISM).orElse(null), schema);
    }


    @Override
    public String factoryIdentifier() {
        return IDENTIFIER;
    }

    @Override
    public Set<ConfigOption<?>> requiredOptions() {
        return new HashSet<>();
    }

    @Override
    public Set<ConfigOption<?>> optionalOptions() {
        Set<ConfigOption<?>> options = new HashSet<>();
        options.add(FactoryUtil.SINK_PARALLELISM);
        return options;
    }
}
